	$(document).ready(function(){

		//Adicionar titulo no banner da pagina
		$("#tituloPagina").text("Soluções de Interior");
		$("#textoTitulo").text("Temos para lhe oferecer uma ampla gama de soluções de interior.");

		/* hover no desktop e click no mobile */

		var event = ('ontouchstart' in window) ? 'click' : 'mouseenter mouseleave';

		$('.hover').on(event, function(){

			$('.hover').find('p').css('visibility','hidden');

			if( $(this).find('p').attr('visible') == 'false' ){
				$(this).find('p').attr('visible', 'true');
				$(this).find('p').css('visibility', 'visible');
			}else{
				$(this).find('p').attr('visible', 'false');
				$(this).find('p').css('visibility', 'hidden');
			}
			
		});


		$("#faContact").attr("href","../website");

		/* Mudar menu */

		$("#ulMenu").html("<li><a href=\"../website\" class=\"active\"> Home</a></li>\
